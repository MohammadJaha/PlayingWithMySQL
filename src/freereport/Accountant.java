    public void showa(){
        ArrayList<GetSt> ar=ac.shows();
        tb=(DefaultTableModel)Students_Table.getModel();
        Object s[]=new Object[5];
        for (int i=0;i<ar.size();i++){
            s[0]=ar.get(i).getid();
            s[1]=ar.get(i).getFname();
            s[2]=ar.get(i).getLname();
            s[3]=ar.get(i).getGrade();
            s[4]=ar.get(i).getDueFee();
            tb.addRow(s);
        }
    }
