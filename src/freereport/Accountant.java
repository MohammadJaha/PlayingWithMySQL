    private void Edit_StActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Edit_StActionPerformed
        // TODO add your handling code here:
        String st=JOptionPane.showInputDialog("Enter Student ID");
        int id=Integer.parseInt(st);
        int ck=ac.chickst(id);
        if(ck==1){
            JOptionPane.showMessageDialog(null,"You Will Change This Student Information:\n"+ ac.StInfo(id));
            String fname=JOptionPane.showInputDialog("Enter Student First Name");
            String lname=JOptionPane.showInputDialog("Enter Student Last Name");
            String grade=JOptionPane.showInputDialog("Enter Student Grade");
            double gr=Double.parseDouble(grade);
            JOptionPane.showMessageDialog(null,ac.editst(id,fname,lname,gr));
            ReLoad();
        }
        else
            JOptionPane.showMessageDialog(null, "Student Not Excest");
    }//GEN-LAST:event_Edit_StActionPerformed

