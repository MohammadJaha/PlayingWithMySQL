    public void ReLoad(){
        tb=(DefaultTableModel)Students_Table.getModel();
        Students_Table.selectAll();
        int count=Students_Table.getSelectedRowCount();
        for(int i=0;i<count;i++){
            tb.removeRow(Students_Table.getSelectedRow());
        }
        showa();
    }

