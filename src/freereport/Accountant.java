    private void Delete_StActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Delete_StActionPerformed
        // TODO add your handling code here:
        if(Students_Table.getSelectedRowCount()==1){
            int ra=Students_Table.getSelectedRow();
            int id=(Integer)(Students_Table.getValueAt(ra, 0));
            int ck=ac.chickst(id);
            String info=ac.StInfo(id);
            ac.deletest(id);
            JOptionPane.showMessageDialog(null, info+"\nDeleted Successfully\n");
            ReLoad();
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Select one Student From the Table to Delete");
            Students_Table.clearSelection();
        }
    }//GEN-LAST:event_Delete_StActionPerformed

