    public void showa(){
        ArrayList<GetAc> ar=ad.showc();
        tb=(DefaultTableModel)Accountants_Table.getModel();
        Object s[]=new Object[3];
        for (int i=0;i<ar.size();i++){
            s[0]=ar.get(i).getid();
            s[1]=ar.get(i).getFname();
            s[2]=ar.get(i).getLname();
            tb.addRow(s);
        }
    }
