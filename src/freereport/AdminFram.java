    private void Edit_AcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Edit_AcActionPerformed
        // TODO add your handling code here:
        String st=JOptionPane.showInputDialog("Enter Accountant ID");
        int id=Integer.parseInt(st);
        int ck=ad.chickac(id);
        if(ck==1){
            JOptionPane.showMessageDialog(null,"You Will Change This Accountant Information:\n"+ ad.AcInfo(id));
            String fname=JOptionPane.showInputDialog("Enter Accountant First Name");
            String lname=JOptionPane.showInputDialog("Enter Accountant Last Name");
            JOptionPane.showMessageDialog(null,ad.editac(id,fname,lname));
            ReLoad();
        }
        else
            JOptionPane.showMessageDialog(null, "Accountant Not Excest");
    }//GEN-LAST:event_Edit_AcActionPerformed

    private void Delete_AcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Delete_AcActionPerformed
        // TODO add your handling code here:
        if(Accountants_Table.getSelectedRowCount()==1){
            int ra=Accountants_Table.getSelectedRow();
            int id=(Integer)(Accountants_Table.getValueAt(ra, 0));
            int ck=ad.chickac(id);
            String info=ad.AcInfo(id);
            ad.deleteac(id);
            JOptionPane.showMessageDialog(null, info+"\nDeleted Successfully\n");
            ReLoad();
        }
        else{
            JOptionPane.showMessageDialog(null, "Please Select one Accountant From the Table to Delete");
            Accountants_Table.clearSelection();
        }
    }//GEN-LAST:event_Delete_AcActionPerformed

